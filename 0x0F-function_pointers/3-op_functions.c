#include "3-calc.h"

/**
 * op_add - A function that adds two integers.
 *
 * @a: The first int.
 * @b: The second int.
 *
 * Return: The summation
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - A function that subtracts two integers.
 *
 * @a: The first int.
 * @b: The second int.
 *
 * Return: The difference.
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - A function that multiplies two integer.
 *
 * @a: The first int.
 * @b: The secomnd int.
 *
 * Return: The products.
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - A function that divides two integers.
 *
 * @a: The first int.
 * @b: The second int.
 *
 * Return: The quotient.
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - A function that returns the modulus of two integers.
 *
 * @a: First int.
 * @b: Second int.
 *
 * Return: The remainder.
*/
int op_mod(int a, int b)
{
	return (a % b);
}
